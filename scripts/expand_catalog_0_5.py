def format_category(cat_num: int, cat_id: str, name_he: str, standard: str, elements: str, issues: list) -> str:
    lines = [
        f"\n#### CATEGORY_{cat_num}: {cat_id} ({name_he})",
        f"* **Standard_ID:** {standard}",
        f"* **Target_Elements:** {elements}",
    ]
    for row in issues:
        lines.append(ISSUE_BLOCK.format(
            issue_id=row[0], name=row[1], standard=row[2], severity=row[3],
            report=row[4], impact=row[5], action=row[6],
        ).rstrip())
    return "\n".join(lines) + "\n"


def append_to_category(content: str, cat_marker: str, new_issues: list) -> str:
    """Insert new issues before next #### CATEGORY or end."""
    idx = content.find(cat_marker)
    if idx < 0:
        raise ValueError(f"Category not found: {cat_marker}")
    rest = content[idx + len(cat_marker) :]
    next_cat = rest.find("\n#### CATEGORY_")
    insert_at = idx + len(cat_marker) + (next_cat if next_cat >= 0 else len(rest))
    block = ""
    for row in new_issues:
        block += ISSUE_BLOCK.format(
            issue_id=row[0], name=row[1], standard=row[2], severity=row[3],
            report=row[4], impact=row[5], action=row[6],
        )
    return content[:insert_at] + block + content[insert_at:]


def bump_version(content: str) -> str:
    return content.replace("# Version: 1.0.0", "# Version: 1.1.0", 1)


def expand_structural():
    path = MD / "קובץ מפרט- שלד, קונסטרוקציה ובטונים.md"
    text = path.read_text(encoding="utf-8")
    text = bump_version(text)
    for cat_id, issues in STRUCTURAL_EXTRA.items():
        marker = f"#### CATEGORY_"
        # find line with cat_id
        import re
        m = re.search(rf"#### CATEGORY_\d+: {cat_id}", text)
        if not m:
            raise ValueError(cat_id)
        text = append_to_category(text, m.group(0), issues)
    cat_num = 4
    for cat_id, (name_he, std, elems, issues) in NEW_STRUCTURAL_CATEGORIES.items():
        text += format_category(cat_num, cat_id, name_he, std, elems, issues)
        cat_num += 1
    path.write_text(text, encoding="utf-8")
    print(f"Structural: {text.count('##### Issue_ID:')} issues")


